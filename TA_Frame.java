    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        
        try
        {            
            for(int i=0;i<jTable1.getRowCount();i++)
            {
                String rsuid=jTable1.getValueAt(i,0).toString().trim();
                jTextArea1.append("=====================================\n         RSU - "+rsuid.trim()+"\n=====================================\n");
                String averageflowrate=jTable1.getValueAt(i,2).toString().trim();
                int afr=Integer.parseInt(averageflowrate.trim());
            
                String ro="";
                for(int j=0;j<afr;j++)
                {
                    String rc="";
                    for(int k=0;k<3;k++)
                    {
                        String psu=RandomStringGenerator.generateRandomString(10,RandomStringGenerator.Mode.ALPHANUMERIC);
                        while(norep.contains(psu.trim()))
                        {
                            psu=RandomStringGenerator.generateRandomString(10,RandomStringGenerator.Mode.ALPHANUMERIC);
                        }
                        norep.add(psu.trim());
                        rc=rc+psu.trim()+"@";
                    }
                    String rcc=rc.substring(0,rc.lastIndexOf('@'));
                    ro=ro+rcc.trim()+"\n";
                }
                String pseduonymset=ro.substring(0,ro.lastIndexOf('\n'));
                jTextArea1.append(pseduonymset.trim().replaceAll("@","\n")+"\n\n");
                allRSU.add(rsuid.trim());
                allPseudonymsets.add(pseduonymset.trim());
            }
            jButton1.setEnabled(false);
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }                                        

